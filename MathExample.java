public class MathExample {

    public static void main(String[] args) {
        System.out.println("Max of 10 and 20 is : " + Math.max(10, 20));
        System.out.println("Min of 10 and 20 is : " + Math.min(10, 20));

        System.out.println("Square root of 4 is : " + Math.sqrt(4));
        System.out.println("Power of 4 and 2 is : " + Math.pow(4, 2));

        System.out.println("Absolute of 10 is : " + Math.abs(10));
        System.out.println("Absolute of -10 is : " + Math.abs(-10));

        System.out.println("Rounded of 10.3 is : " + Math.round(10.3));
        System.out.println("Rounded of 10.5 is : " + Math.round(10.5));
        System.out.println("Rounded of 10.7 is : " + Math.round(10.7));

        System.out.println("Ceil of 10.3 is : " + Math.ceil(10.3));
        System.out.println("Ceil of 10.5 is : " + Math.ceil(10.5));
        System.out.println("Ceil of 10.7 is : " + Math.ceil(10.7));

        System.out.println("Floor of 10.3 is : " + Math.floor(10.3));
        System.out.println("Floor of 10.5 is : " + Math.floor(10.5));
        System.out.println("Floor of 10.7 is : " + Math.floor(10.7));

        System.out.println("Random number is : " + Math.random()); // Between 0 and 1

        System.out.println("PI value : " + Math.PI);
        System.out.println("E value : " + Math.E);

        System.out.println("Sin of 90 is : " + Math.sin(90));
        System.out.println("Cos of 90 is : " + Math.cos(90));
        System.out.println("Tan of 90 is : " + Math.tan(90));
    }
}
